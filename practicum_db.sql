CREATE DEFINER=`root`@`localhost` PROCEDURE `GetTotalStudents`(

IN p_instructor INT,

IN p_fullname VARCHAR(150),

IN p_eligible INT,

IN p_campus VARCHAR(50),

IN p_year INT,

IN p_limit INT

)
BEGIN

	IF p_limit IS NULL OR p_limit <= 0 THEN

		SET p_limit = 2147483647;

    END IF;

    

	SELECT COUNT(studentId) AS total FROM students

	WHERE (p_instructor IS NULL OR instructor = p_instructor)

    AND (p_fullname IS NULL OR LOWER(CONCAT(firstName, ' ', lastName)) LIKE LOWER(CONCAT('%', p_fullname, '%')))

    AND (p_eligible IS NULL OR eligible = p_eligible)

	AND (p_campus IS NULL OR FIND_IN_SET(campus, p_campus) > 0)

    AND ((p_year IS NULL) OR (p_year = 0) OR (year = p_year))

    ORDER BY campus, lastName

    LIMIT p_limit;

END ;;
